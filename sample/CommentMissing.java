package sample;

public class CommentMissing {
    private int x, y;

    public CommentMissing(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int add() {
        return x + y;
    }

    public int subtract() {
        return x - y;
    }

    public int multiply() {
        return x * y;
    }
}
